package com.example.mymod.spell;

/**
 * Школы магии мода. Определяют цветовую схему, стиль и принадлежность нод и заклинаний.
 * Каждая школа имеет уникальный цвет для рендеринга в дереве прокачки.
 */
public enum MagicSchool {

    /** Нейтральная — для центральной ноды и прочих не-школьных элементов */
    NONE(0xE8D5A0, "Нейтральная"),

    /** Багровый путь — магия разрушения: огонь, молния, лёд */
    CRIMSON(0xFF3A1A, "Багровый путь"),

    /** Путь Тимофея — святая магия: свет, исцеление, руны */
    HOLY(0xFFD700, "Путь Тимофея"),

    /** Путь Культиста — тёмная магия: проклятья, яд, некромантия */
    CULTIST(0xA020F0, "Путь Культиста"),

    /** Путь Марионетки — магия призыва: фамильяры, симбиоз */
    PUPPET(0x00CC88, "Путь Марионетки");

    /** Основной цвет школы в формате 0xRRGGBB (без альфа-канала) */
    public final int color;

    /** Отображаемое название школы (на русском) */
    public final String displayName;

    MagicSchool(int color, String displayName) {
        this.color = color;
        this.displayName = displayName;
    }

    /** Получить цвет с заданным альфа-каналом */
    public int colorWithAlpha(int alpha) {
        return ((alpha & 0xFF) << 24) | (color & 0xFFFFFF);
    }
}
